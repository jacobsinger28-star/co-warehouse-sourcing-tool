def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default=None)
    ap.add_argument("--review", action="store_true",
                    help="build the shareable review copy (feedback controls + framing)")
    ap.add_argument("--from-json", default=None, metavar="PATH",
                    help="build from a saved JSON data snapshot instead of the DB â€” "
                         "regenerate the HTML on a machine with no Postgres")
    ap.add_argument("--dump-json", default=None, metavar="PATH",
                    help="also write the collected DB data to this JSON path â€” a reusable, "
                         "DB-free snapshot you can later rebuild from with --from-json")
    args = ap.parse_args()
    out = Path(args.out) if args.out else Path(
        "exports/dashboard_review.html" if args.review else "exports/dashboard.html")

    if args.from_json:
        data = json.loads(Path(args.from_json).read_text(encoding="utf-8"))
        data.setdefault("comp_max", _comp_max())  # backfill snapshots saved before the breakdown bars existed
        data.setdefault("markets", [])             # backfill pre-multi-market snapshots
        data.setdefault("city_ceil", {})           # backfill pre-A1 snapshots (client falls back to a global ceiling)
        data.setdefault("city_live", {})
    else:
        data = collect()
        if args.dump_json:
            Path(args.dump_json).parent.mkdir(parents=True, exist_ok=True)
            Path(args.dump_json).write_text(
                json.dumps(data, default=_js), encoding="utf-8")
            print(f"data snapshot: {args.dump_json} written "
                  f"({len(data['rows'])} rows, no DB needed to rebuild)")

    payload = json.dumps(data, default=_js).replace("</", "<\\/")
    html = (TEMPLATE
            .replace("__DATA__", payload)
            .replace("__GEN__", data["generated"])
            .replace("__REVIEW__", REVIEW_BANNER if args.review else "")
            .replace("__REVIEW_FLAG__", "true" if args.review else "false"))
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(html, encoding="utf-8")
    mode = "review copy" if args.review else "dashboard"
    print(f"{mode}: {out} written â€” {data['n_universe']} universe + "
          f"{data['n_manual']} manual-review rows ({out.stat().st_size//1024} KB)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
